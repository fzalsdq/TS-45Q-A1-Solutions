// Ordinal Numbers 
let O=[1,2,3,4,5,6,7,8,9];
let i=0;
for (i=0; i<O.length; i++){
    if (O[i]==1 ){
        console.log(O[i]+"st");
    }
   else if (O[i]==2 ){
        console.log(O[i]+"nd");
    }

    else if (O[i]==3 ){
        console.log(O[i]+"rd");
    }

    else {
        console.log(O[i]+"th");
    }
}



